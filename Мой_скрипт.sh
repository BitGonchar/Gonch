# Простой Bash-скрипт с произвольными командами

echo "=== Начало работы скрипта ==="

# 1. Вывод информации о системе
echo "1. Информация о системе:"
uname -a
echo "-----"

# 2. Текущая дата и время
echo "2. Текущая дата и время:"
date
echo "-----"

# 3. Создание временного файла
echo "3. Создаем временный файл..."
TEMP_FILE="/tmp/example_temp_file_$(date +%s).txt"
echo "Это тестовый файл, созданный скриптом." > $TEMP_FILE
echo "Файл создан: $TEMP_FILE"
echo "-----"

# 4. Проверка содержимого файла
echo "4. Содержимое созданного файла:"
cat $TEMP_FILE
echo "-----"

# 5. Завершение работы
echo "5. Завершение работы скрипта"
echo "=== Скрипт завершен ==="
